def fill(locale, strings=None):
    path = Path("translations") / locale / "LC_MESSAGES" / "messages.po"
    with path.open("r", encoding="utf-8") as file:
        catalog = pofile.read_po(file)

    messages = [message for message in catalog if message.id]
    if strings is not None and len(strings) != len(messages):
        raise RuntimeError(f"{locale} translation count mismatch: {len(strings)} != {len(messages)}")

    for index, message in enumerate(messages):
        message.string = message.id if strings is None else strings[index]
        message.flags.discard("fuzzy")

    with path.open("wb") as file:
        pofile.write_po(file, catalog, width=100)


